function btnSaveShiftClick() {
    debugger;
    if ($("#ShiftCode").val() == "") {
        ShowError("Vui lòng nhập mã");
        $("#ShiftCode").focus();
        return false;
    }

    if ($("#ShiftName").val() == "") {
        ShowError("Vui lòng nhập tên ca");
        $("#ShiftName").focus();
        return false;
    }
}